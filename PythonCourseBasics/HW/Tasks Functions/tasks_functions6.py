""" DESCRIPTION:
Write a function 'find_max' that takes a list of numbers and returns the largest number in the list.
"""
def find_max(numbers):
    return max(numbers)

### TEST:
print(find_max([1, 3, 2, 8, 5]))

### EXPECTED OUTPUT:
# 8
